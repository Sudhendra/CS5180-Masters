def run_multiple_trials(env, num_trials, num_episodes, discount_factor, epsilon):
    all_rewards = np.zeros((num_trials, num_episodes))  # Initialize an array to hold rewards for each trial and episode
    
    for trial in trange(num_trials):
        _, _, rewards = on_policy_mc_control_epsilon_soft(env, num_episodes, discount_factor, epsilon)
        all_rewards[trial] = rewards  # Store the rewards of each episode for the trial
        
    return all_rewards

def plot_learning_curves_with_confidence_intervals(all_rewards):
    """
    Plots the learning curve with confidence intervals.
    
    Args:
        all_rewards: A 2D array with shape (num_trials, num_episodes) containing the total rewards per episode for each trial.
    """
    mean_rewards = np.mean(all_rewards, axis=0)
    std_rewards = np.std(all_rewards, axis=0)
    episodes = np.arange(all_rewards.shape[1])
    
    plt.figure(figsize=(10, 5))
    plt.fill_between(episodes, mean_rewards - std_rewards, mean_rewards + std_rewards, alpha=0.2, color="blue")
    plt.plot(episodes, mean_rewards, 'b-', label='Mean Total Reward')
    plt.xlabel('Episode')
    plt.ylabel('Total Reward')
    plt.title('Learning Curve with Confidence Intervals')
    plt.legend()
    plt.draw()
    plt.savefig("q5at0.png")

