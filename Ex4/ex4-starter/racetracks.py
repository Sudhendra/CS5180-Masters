class TrackEnvironment(gym.Env):
    """
    Custom Environment that follows gym interface.
    This is a simple grid environment where the agent has to reach the goal from the start position.
    """
    metadata = {'render.modes': ['human']}

    def __init__(self, track):
        super(TrackEnvironment, self).__init__()
        self.track = track
        self.start_positions = np.argwhere(track == 2)
        self.goal_positions = np.argwhere(track == 3)
        self.current_position = None
        
        # Define action and observation space
        # They must be gym.spaces objects
        self.action_space = spaces.Discrete(4)  # Up, down, left, right
        # Use int directly instead of np.int
        self.observation_space = spaces.Box(low=0, high=max(track.shape), shape=(2,), dtype=int)
        
    def reset(self):
        # Reset the state of the environment to an initial state
        idx = np.random.choice(len(self.start_positions))
        self.current_position = self.start_positions[idx].copy()
        initial_state = (self.current_position[0], self.current_position[1], False)
        return initial_state

    def step(self, action):
      # Initialize next_position based on the current position to ensure it's always defined
      next_position = self.current_position.copy()

      # Update next_position based on the action
      if action == 0:  # up
          next_position[0] -= 1
      elif action == 1:  # down
          next_position[0] += 1
      elif action == 2:  # left
          next_position[1] -= 1
      elif action == 3:  # right
          next_position[1] += 1

      # Check if the next position is valid
      reward = -1  # Default reward
      done = False
      info = {}  # Dictionary for additional information
      
      # Validate the next position
      if self.is_valid_position(next_position):
          self.current_position = next_position
          if self.track[tuple(next_position)] == 3:
              done = True
              reward = 0  # Assign a reward for reaching the goal
      else:
          done = True  # If an invalid position is encountered, end the episode
      
      # Format the observation to match Gym's standard (here, it's the current position as a tuple)
      observation = tuple(self.current_position)

      # Return the standard Gym step method output format
      return observation, reward, done, info

    def render(self, mode='human', close=False):
        # Render the environment to the screen
        # For simplicity, this example will not implement rendering
        pass

    def is_valid_position(self, position):
        # Check if the position is within bounds and not an obstacle
        if (0 <= position[0] < self.track.shape[0]) and (0 <= position[1] < self.track.shape[1]):
            return self.track[tuple(position)] in [1, 3]  # drivable or goal
        return False

def initialize_track_environment(track):
    return TrackEnvironment(track)


